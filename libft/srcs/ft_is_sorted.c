#include "../includes/libft.h"

int		ft_is_sorted(int *arr, int n)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	if (n == 0 || n == 1)
		return (1);
	while (i < n - 1)
	{
		if (arr[i] <= arr[i + 1])
			i++;
		else
			return (0);
	}
	return (1);
}
